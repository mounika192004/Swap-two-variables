public class Swap {
    public static void main(String[] args){
        int a = 10;
        int b = 20;
        int c;
        System.out.printf("Before swapping a=%d, b=%d",a,b);
        c = a;
        a = b;
        b = c;
        System.out.printf("\nAfter swapping a=%d, b=%d",a,b);

    }
    
}
